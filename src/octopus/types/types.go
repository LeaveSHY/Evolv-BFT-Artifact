// ValidatorSet represents a set of validators for a specific epoch
type ValidatorSet struct {
	Epoch      uint64
	Validators map[uint64]*Validator
	TotalPower uint64
	QuorumSize uint64
}

// NewValidatorSet creates a new validator set.
// TotalPower is computed by summing the Power of active validators only,
// ensuring correct quorum thresholds when validators have varying weights
// or some are marked inactive.
func NewValidatorSet(epoch uint64, validators map[uint64]*Validator) *ValidatorSet {
	var totalPower uint64
	for _, v := range validators {
		if v != nil && v.IsActive {
			p := v.Power
			if p == 0 {
				p = 1 // default power for active validators without explicit weight
			}
			totalPower += p
		}
	}
	if totalPower == 0 {
		// Fallback: if no validator has IsActive set (e.g., during bootstrap),
		// count all validators with unit power to avoid zero quorum.
		totalPower = uint64(len(validators))
	}
	quorumSize := totalPower*2/3 + 1

	return &ValidatorSet{
		Epoch:      epoch,
		Validators: validators,
		TotalPower: totalPower,
		QuorumSize: quorumSize,
	}
}

// Copy returns a deep copy of the ValidatorSet
func (vs *ValidatorSet) Copy() *ValidatorSet {
	if vs == nil {
		return nil
	}
	newValidators := make(map[uint64]*Validator, len(vs.Validators))
	for k, v := range vs.Validators {
		copied := *v
		if v.PublicKey != nil {
			copied.PublicKey = make(PublicKey, len(v.PublicKey))
			copy(copied.PublicKey, v.PublicKey)
		}
		if v.VRFPublicKey != nil {
			copied.VRFPublicKey = append([]byte(nil), v.VRFPublicKey...)
		}
		newValidators[k] = &copied
	}
	return &ValidatorSet{
		Epoch:      vs.Epoch,
		Validators: newValidators,
		TotalPower: vs.TotalPower,
		QuorumSize: vs.QuorumSize,
	}
}

func (vs *ValidatorSet) Hash() []byte {
	if vs == nil {
		return nil
	}
	cfg := &Configuration{
		ID:         vs.Epoch,
		Validators: vs.Validators,
		QuorumSize: vs.QuorumSize,
	}
	return cfg.Hash()
}

func VoteSigningBytes(blockID Hash, view uint64, epoch uint64, configID uint64, lane uint64) []byte {
	buf := make([]byte, 0, len(blockID)+32)
	tmp := make([]byte, 8)

	buf = append(buf, blockID[:]...)
	binary.BigEndian.PutUint64(tmp, view)
	buf = append(buf, tmp...)
	binary.BigEndian.PutUint64(tmp, epoch)
	buf = append(buf, tmp...)
	binary.BigEndian.PutUint64(tmp, configID)
	buf = append(buf, tmp...)
	binary.BigEndian.PutUint64(tmp, lane)
	buf = append(buf, tmp...)
	return buf
}

func QCSigningBytes(blockHash []byte, view uint64, epoch uint64, configID uint64, lane uint64) []byte {
	var blockID Hash
	copy(blockID[:], blockHash)
	return VoteSigningBytes(blockID, view, epoch, configID, lane)
}

func TimeoutSigningBytes(view uint64, epoch uint64, configID uint64, lane uint64) []byte {
	buf := make([]byte, 0, 32)
	tmp := make([]byte, 8)

	binary.BigEndian.PutUint64(tmp, view)
	buf = append(buf, tmp...)
	binary.BigEndian.PutUint64(tmp, epoch)
	buf = append(buf, tmp...)
	binary.BigEndian.PutUint64(tmp, configID)
	buf = append(buf, tmp...)
	binary.BigEndian.PutUint64(tmp, lane)
	buf = append(buf, tmp...)
	return buf
}

