// QuorumCertificate represents a quorum certificate
type QuorumCertificate struct {
	mu sync.RWMutex

	BlockHash []byte
	View      uint64
	Epoch     uint64 // Epoch this QC belongs to
	ConfigID  uint64
	Lane      uint64
	Phase     Phase

	// Signatures from validators
	Signatures map[uint64][]byte

	// Aggregated signature
	AggregatedSig []byte

	// Number of signatures
	NumSignatures int
}

// NewQuorumCertificate creates a new QC
func NewQuorumCertificate(blockHash []byte, view uint64, epoch uint64, phase Phase) *QuorumCertificate {
	return NewQuorumCertificateWithIdentity(blockHash, view, epoch, epoch, 0, phase)
}

func NewQuorumCertificateWithIdentity(blockHash []byte, view uint64, epoch uint64, configID uint64, lane uint64, phase Phase) *QuorumCertificate {
	return &QuorumCertificate{
		BlockHash:  blockHash,
		View:       view,
		Epoch:      epoch,
		ConfigID:   configID,
		Lane:       lane,
		Phase:      phase,
		Signatures: make(map[uint64][]byte),
	}
}

// AddSignature adds a signature to the QC
func (qc *QuorumCertificate) AddSignature(validatorID uint64, signature []byte) {
	qc.mu.Lock()
	defer qc.mu.Unlock()
	if _, exists := qc.Signatures[validatorID]; !exists {
		qc.Signatures[validatorID] = signature
		qc.NumSignatures++
	}
}

// GetBlockHash returns the block hash
func (qc *QuorumCertificate) GetBlockHash() []byte {
	qc.mu.RLock()
	defer qc.mu.RUnlock()
	return qc.BlockHash
}

// GetView returns the view
func (qc *QuorumCertificate) GetView() uint64 {
	qc.mu.RLock()
	defer qc.mu.RUnlock()
	return qc.View
}

// GetEpoch returns the epoch
func (qc *QuorumCertificate) GetEpoch() uint64 {
	qc.mu.RLock()
	defer qc.mu.RUnlock()
	return qc.Epoch
}

// GetNumSignatures returns the number of signatures
func (qc *QuorumCertificate) GetNumSignatures() int {
	qc.mu.RLock()
	defer qc.mu.RUnlock()
	return qc.NumSignatures
}

// IsComplete checks if the QC has enough signatures
func (qc *QuorumCertificate) IsComplete(quorumSize uint64) bool {
	qc.mu.RLock()
	defer qc.mu.RUnlock()
	return uint64(qc.NumSignatures) >= quorumSize
}

