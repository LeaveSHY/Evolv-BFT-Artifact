// TimeoutVote is broadcast by a node when its view timer expires.
// Collecting 2f+1 TimeoutVotes for the same view forms a TimeoutCertificate.
type TimeoutVote struct {
	View      uint64
	Epoch     uint64
	ConfigID  uint64
	Lane      uint64
	VoterID   uint64
	HighestQC *QuorumCertificate // The voter's highest known QC
	Signature []byte
}

// TimeoutCertificate is an aggregated proof that 2f+1 nodes have timed out
// on the same view. It enables the next leader to safely propose without
// waiting for the previous view's QC (liveness under Byzantine leaders).
type TimeoutCertificate struct {
	View       uint64
	Epoch      uint64
	ConfigID   uint64
	Lane       uint64
	HighestQC  *QuorumCertificate // The highest QC among all timeout voters
	Signatures map[uint64][]byte  // voterID -> signature
	NumVoters  int
}

