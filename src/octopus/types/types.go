// ReconfigData represents the payload for a reconfiguration transaction
// Manual join/leave requests are individually signed by the requester.
// Auto-leave requests are authenticated by a Hydra quorum proof.
type ReconfigData struct {
	Type           ReconfigType
	NodeID         uint64
	PublicKey      PublicKey
	VRFPublicKey   []byte
	Power          uint64
	Epoch          uint64
	TargetEpoch    uint64
	Signature      Signature
	AutoLeaveProof *HydraTransitionProof
}

func ReconfigSigningBytes(data *ReconfigData) []byte {
	if data == nil {
		return nil
	}
	type reconfigSigningData struct {
		Type         ReconfigType `json:"type"`
		NodeID       uint64       `json:"node_id"`
		PublicKey    []byte       `json:"public_key,omitempty"`
		VRFPublicKey []byte       `json:"vrf_public_key,omitempty"`
		Power        uint64       `json:"power"`
		Epoch        uint64       `json:"epoch"`
		TargetEpoch  uint64       `json:"target_epoch"`
	}
	payload, _ := json.Marshal(reconfigSigningData{
		Type:         data.Type,
		NodeID:       data.NodeID,
		PublicKey:    append([]byte(nil), data.PublicKey...),
		VRFPublicKey: append([]byte(nil), data.VRFPublicKey...),
		Power:        data.Power,
		Epoch:        data.Epoch,
		TargetEpoch:  data.TargetEpoch,
	})
	return payload
}

func (d *ReconfigData) Sign(privateKey PrivateKey) {
	if d == nil {
		return
	}
	d.Signature = ed25519.Sign(privateKey, ReconfigSigningBytes(d))
}

func (d *ReconfigData) VerifySignature() bool {
	if d == nil {
		return false
	}
	switch d.Type {
	case ReconfigJoin, ReconfigLeave:
		return ed25519.Verify(d.PublicKey, ReconfigSigningBytes(d), d.Signature)
	default:
		return false
	}
}

