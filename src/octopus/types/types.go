// Block represents a block in the DAG
type Block struct {
	mu sync.RWMutex

	// Core fields
	Height uint64
	Hash   []byte
	Parent []byte
	Data   []byte

	JoinRequests  []PublicKey
	LeaveRequests []PublicKey

	// Payload (Changed from Txs to VertexCertificates for Multi-Leader)
	Payload []*VertexCertificate

	// Pipelined fields
	View  uint64
	Phase Phase
	Epoch uint64 // Epoch this block belongs to

	// Multi-Leader fields
	LeaderID uint64
	Rank     int64
	LaneID   uint64
	ConfigID uint64

	// QC
	Justify *QuorumCertificate

	// TC: Timeout Certificate from the previous view (present when the
	// previous leader timed out and liveness required TC-based view advance)
	TimeoutCert *TimeoutCertificate

	// Metadata
	Timestamp int64

	// Phase 5: Random Beacon (Seed for next view/epoch)
	Randomness []byte
}

// NewBlock creates a new block
func NewBlock(height uint64, parent []byte, data []byte, view uint64, epoch uint64, leaderID uint64, rank int64, justify *QuorumCertificate, randomness []byte) *Block {
	block := &Block{
		Height:     height,
		Parent:     parent,
		Data:       data,
		View:       view,
		Epoch:      epoch,
		Phase:      PhasePrepare,
		LeaderID:   leaderID,
		Rank:       rank,
		Justify:    justify,
		Payload:    make([]*VertexCertificate, 0),
		Timestamp:  time.Now().UnixNano(),
		Randomness: randomness,
	}
	block.Hash = block.ComputeHash()
	return block
}

func (b *Block) ComputeHash() []byte {
	payloadCap := 0
	for _, cert := range b.Payload {
		if cert != nil {
			payloadCap += len(cert.VertexHash) + 16
		}
	}
	buffer := make([]byte, 0, 128+len(b.Parent)+len(b.Data)+len(b.Randomness)+payloadCap)
	u64 := make([]byte, 8)
	i64 := make([]byte, 8)

	binary.BigEndian.PutUint64(u64, b.Height)
	buffer = append(buffer, u64...)
	buffer = append(buffer, b.Parent...)
	buffer = append(buffer, b.Data...)

	binary.BigEndian.PutUint64(u64, b.View)
	buffer = append(buffer, u64...)
	binary.BigEndian.PutUint64(u64, b.Epoch)
	buffer = append(buffer, u64...)
	binary.BigEndian.PutUint64(u64, b.LeaderID)
	buffer = append(buffer, u64...)
	binary.BigEndian.PutUint64(i64, uint64(b.Rank))
	buffer = append(buffer, i64...)
	binary.BigEndian.PutUint64(u64, b.LaneID)
	buffer = append(buffer, u64...)
	binary.BigEndian.PutUint64(u64, b.ConfigID)
	buffer = append(buffer, u64...)
	binary.BigEndian.PutUint64(i64, uint64(b.Timestamp))
	buffer = append(buffer, i64...)
	buffer = append(buffer, b.Randomness...)

	if b.Justify != nil {
		buffer = append(buffer, b.Justify.BlockHash...)
		binary.BigEndian.PutUint64(u64, b.Justify.View)
		buffer = append(buffer, u64...)
		binary.BigEndian.PutUint64(u64, b.Justify.Epoch)
		buffer = append(buffer, u64...)
	}

	binary.BigEndian.PutUint64(u64, uint64(len(b.Payload)))
	buffer = append(buffer, u64...)
	for _, cert := range b.Payload {
		if cert == nil {
			binary.BigEndian.PutUint64(u64, 0)
			buffer = append(buffer, u64...)
			continue
		}
		buffer = append(buffer, cert.VertexHash[:]...)
		binary.BigEndian.PutUint64(u64, cert.Epoch)
		buffer = append(buffer, u64...)
		binary.BigEndian.PutUint64(u64, cert.Round)
		buffer = append(buffer, u64...)
	}

	hash := sha256.Sum256(buffer)
	return hash[:]
}

