// PublicKey is an Ed25519 public key
type PublicKey = ed25519.PublicKey

// PrivateKey is an Ed25519 private key
type PrivateKey = ed25519.PrivateKey

// Signature is an Ed25519 signature
type Signature []byte

// Hash is a SHA256 hash
type Hash [32]byte

// Phase represents the phase in Pipelined HotStuff
type Phase int

const (
	PhasePrepare Phase = iota
	PhasePrecommit
	PhaseCommit
	PhaseDecide
)

func (p Phase) String() string {
	switch p {
	case PhasePrepare:
		return "PREPARE"
	case PhasePrecommit:
		return "PRECOMMIT"
	case PhaseCommit:
		return "COMMIT"
	case PhaseDecide:
		return "DECIDE"
	default:
		return "UNKNOWN"
	}
}

