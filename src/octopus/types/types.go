// Validator represents a validator in the network
type Validator struct {
	ID           uint64
	PublicKey    PublicKey
	VRFPublicKey []byte
	Power        uint64
	IsActive     bool
}

type Configuration struct {
	ID         uint64
	Validators map[uint64]*Validator
	QuorumSize uint64
}

func (c *Configuration) Hash() []byte {
	if c == nil {
		return nil
	}
	type validatorHash struct {
		ID           uint64 `json:"id"`
		PublicKey    []byte `json:"public_key"`
		VRFPublicKey []byte `json:"vrf_public_key,omitempty"`
		Power        uint64 `json:"power"`
		IsActive     bool   `json:"is_active"`
	}
	items := make([]validatorHash, 0, len(c.Validators))
	for id, v := range c.Validators {
		if v == nil {
			continue
		}
		items = append(items, validatorHash{
			ID:           id,
			PublicKey:    append([]byte(nil), v.PublicKey...),
			VRFPublicKey: append([]byte(nil), v.VRFPublicKey...),
			Power:        v.Power,
			IsActive:     v.IsActive,
		})
	}
	for i := 0; i < len(items); i++ {
		for j := i + 1; j < len(items); j++ {
			if items[j].ID < items[i].ID {
				items[i], items[j] = items[j], items[i]
			}
		}
	}
	payload, _ := json.Marshal(struct {
		ID         uint64          `json:"id"`
		QuorumSize uint64          `json:"quorum_size"`
		Validators []validatorHash `json:"validators"`
	}{
		ID:         c.ID,
		QuorumSize: c.QuorumSize,
		Validators: items,
	})
	sum := sha256.Sum256(payload)
	return sum[:]
}

func (c *Configuration) ToValidatorSet() *ValidatorSet {
	if c == nil {
		return nil
	}
	validators := make(map[uint64]*Validator, len(c.Validators))
	for id, v := range c.Validators {
		if v == nil {
			continue
		}
		dup := *v
		if v.PublicKey != nil {
			dup.PublicKey = append(PublicKey(nil), v.PublicKey...)
		}
		if v.VRFPublicKey != nil {
			dup.VRFPublicKey = append([]byte(nil), v.VRFPublicKey...)
		}
		validators[id] = &dup
	}
	valSet := NewValidatorSet(c.ID, validators)
	valSet.QuorumSize = c.QuorumSize
	return valSet
}

